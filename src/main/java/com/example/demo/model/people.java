
package com.example.demo.model;

public class people
 {
    private String name;
    private String imgs;

    public people(String name , String imgs) {
        this.name = name;
        this.imgs = imgs;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getimgs() {
        return imgs;
    }

    public void setimgs(String imgs) {
        this.imgs = imgs;
    }


}
